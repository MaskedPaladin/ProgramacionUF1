package per.projects.exercices.dau;

public class Dau {
    private int valor;
    public Dau(int valor) {
        this.valor = valor;
    }
    public int getValor() {
        return valor;
    }
    public void setValor(int valor) {
        this.valor = valor;
    }
    @java.lang.Override
    public java.lang.String toString() {
        return "Dau{" +
                "valor=" + valor +
                '}';
    }
}